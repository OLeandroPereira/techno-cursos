<template>
  <div>
    <h1>Home</h1>
    <p>{{ api }}</p>
  </div>
</template>


<script>
export default {
  name: "HomePage",
  data() {
    return {
      api: null
    };
  },
  methods: {
    fetchData() {
      fetch(`http://localhost:3000/home`)
        .then(r => r.json())
        .then(r => {
          this.api = r;
        });
    }
  },
  created() {
    this.fetchData();
  }
};
</script>
